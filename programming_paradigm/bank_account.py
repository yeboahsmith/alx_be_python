
class BankAccount:
  def __init__ (self, account_balance):
    self.account_balance = account_balance
    self.account_balance = 0

def deposit (amount):
  amount += self.account_balance
  return amount

def withdraw (with_amount):
  WithdrawAmount = self.account_balance - with_amount
  if WithdrawAmount < 0:
    return True
  else:
      return False

